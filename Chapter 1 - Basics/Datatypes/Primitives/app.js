// Primitive Data Types
// Primitive data types are the most basic data types in JavaScript. There are seven primitive data types:

// 1. Number: Represents both integer and floating-point numbers.
let age = 25;          // Integer
let temperature = 98.6; // Floating-point number

// 2. String: Represents a sequence of characters.

let greeting = "Hello, World!";
let name = 'John Doe';

// 3. Boolean: Represents logical entities and can have two values: true or false.

let isJavaScriptFun = true;
let isFinished = false;

// 4. Undefined: Represents a variable that has been declared but has not yet been assigned a value.

let x;
console.log(x); // undefined

// 5. Null: Represents the intentional absence of any object value. It is a primitive value that represents the absence of any value.

let y = null;

// 6. Symbol: Represents a unique and immutable identifier. It is often used for object property keys.

let sym = Symbol('unique');

// 7. BigInt: Represents whole numbers larger than the Number type can represent.

let bigIntNumber = 1234567890123456789012345678901234567890n;